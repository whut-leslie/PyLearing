str = "123456789"
# 1 截取从 2-5的字符串
print(str[2:5])
# 2 截取从2-末尾的字符串
print(str[2:])
# 3 截取从开始-5 位置的字符串
print(str[:-5])
# 4，截取完整的字符串
print(str[:])
# 5 从开始位置，每隔一个字符截取字符串
print(str[::2])
# 6 从索引 1 开始，每隔一个取一个
print(str[1::2])
# 7 截取从2 -末尾-1的字符串
print(str[2:-1])
# 8 截取字符串末尾两个字符
print(str[-2:])
# 9 字符串的逆序
print(str[-1::-1])
print(str[::-1])